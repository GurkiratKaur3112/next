"use client";
import { useEffect } from "react";
import { motion, stagger, useAnimate } from "framer-motion";
import { cn } from "@/lib/utils";

export const TextGenerateEffect = ({
  words,
  className,
}: {
  words: string;
  className?: string;
}) => {
  const [scope, animate] = useAnimate();
  let wordsArray = words.split(" ");
  
  useEffect(() => {
    animate(
      "span",
      {
        opacity: 1,
      },
      {
        duration: 2,
        delay: stagger(0.2),
      }
    );
  }, [scope.current]);

  const renderWords = () => {
    return (
      <motion.div ref={scope}>
        {wordsArray.map((word, idx) => {
          return (
            <motion.span
              key={word + idx}
              className="text-4xl md:text-5xl lg:text-7xl text-center font-extrabold relative z-20 opacity-0 p-2 bg-gradient-to-r from-cyan-400 to-white bg-clip-text text-transparent"
            >
              {word}{" "}
            </motion.span>
          );
        })}
      </motion.div>
    );
  };

  return (
    <div className={cn("font-bold ", className)}>
      <div className="mt-4">
        <h1 className="text-2xl leading-snug tracking-wider w-full text-center">
          {renderWords()}
        </h1>
      </div>
    </div>
  );
};